def get_mid_coord(coord1: str, coord2: str) -> str:
    y1, x1 = map(int, coord1.split('_'))
    y2, x2 = map(int, coord2.split('_'))
    mid_y = (y1 + y2) // 2
    mid_x = (x1 + x2) // 2
    return f"{mid_y:03d}_{mid_x:03d}"

def batch_image_inference(model, img_folder: str, save_folder: str, cfg: str,
                          pairs: List[Tuple[str, str]], batch_name: str = "Batch"):
    print(f"\n===== {batch_name}: Scanning PNG files =====")

    img_files = sorted(glob.glob(os.path.join(img_folder, '*.png')))
    img_map = {os.path.basename(f).replace('.png', ''): f for f in img_files}

    print(f"Found {len(img_files)} images:")

    for name in sorted(img_map.keys())[:10]:
        print(f"  - {name}")
    if len(img_map) > 10:
        print(f"  - ... (Total {len(img_map)} images)")

    for i, (imgA, imgB) in enumerate(pairs):
        if imgA not in img_map or imgB not in img_map:
            print(f"[Warning] Missing images: {imgA} or {imgB}, skipping this pair.")
            continue

        img0_path = img_map[imgA]
        img1_path = img_map[imgB]
        output_coord = get_mid_coord(imgA, imgB)
        output_name = output_coord

        print(f"→ {batch_name} Pair {i + 1}: {imgA}.png + {imgB}.png → {output_name}.png")

        run_inference(model, img0_path, img1_path, save_folder, output_name)

